x = 5
y = 10

# Arithimetic Operators
print("Addition : ",x+y)
print("Subtraction : ",x-y)
print("Multiplication : ",x*y)
print("Division : ",float(x/y))
print("Modulus : ",x%y)
print("Exponential : ",x**y)
print("Floor Division : ",x//y)

# Assignment Operator

a=3 # assign value to a
print("a=3",a)
a+=3 # a=a+3
print("a+=3 : ",a)
a-=3 # a=a-3
print("a-=3 : ",a)

a*=3 # a=a*3
print("a*=3 : ",a)
a/=3 # a=a/3
print("a/=3 : ",a)

a%=3 # a=a+3
print("a%=3 : ",a)
a//=3 # a=a//3
print("a//=3 : ",a)

a**=3 # a=a**3
print("a**=3 : ",a)

